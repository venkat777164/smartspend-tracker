 class task{
    public static void main(String[] args) {
        System.out.println("negative numbers:");
        for (int i = 10; i <= 50; i++) {
            System.out.print(-i + " ");
        }

        System.out.println("\n");

        System.out.println("Negative numbers:");
        int j = 10;
        while (j <= 50) {
            System.out.print(-j + " ");
            j++;
        }
    }
}    
